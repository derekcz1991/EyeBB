package com.twinly.eyebb.model;

public class Kindergarten {

	private int id;
	private String englishName;
	private String tranditionalName;
	private String simplifiedName;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getEnglishName() {
		return englishName;
	}

	public void setEnglishName(String englishName) {
		this.englishName = englishName;
	}

	public String getTranditionalName() {
		return tranditionalName;
	}

	public void setTranditionalName(String tranditionalName) {
		this.tranditionalName = tranditionalName;
	}

	public String getSimplifiedName() {
		return simplifiedName;
	}

	public void setSimplifiedName(String simplifiedName) {
		this.simplifiedName = simplifiedName;
	}

}
